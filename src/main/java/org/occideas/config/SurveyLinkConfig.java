package org.occideas.config;

public class SurveyLinkConfig {

    private String surveyLink;
    private String surveyInfoLink;
    private String emailLink;

    public String getSurveyLink() {
        return surveyLink;
    }

    public void setSurveyLink(String surveyLink) {
        this.surveyLink = surveyLink;
    }

    public String getSurveyInfoLink() {
        return surveyInfoLink;
    }

    public void setSurveyInfoLink(String surveyInfoLink) {
        this.surveyInfoLink = surveyInfoLink;
    }

    public String getEmailLink() {
        return emailLink;
    }

    public void setEmailLink(String emailLink) {
        this.emailLink = emailLink;
    }
}
