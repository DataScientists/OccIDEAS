package org.occideas.config;

public class NodeSurveyConfig {

    private String email;
    private String lastShiftHours;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getLastShiftHours() {
        return lastShiftHours;
    }

    public void setLastShiftHours(String lastShiftHours) {
        this.lastShiftHours = lastShiftHours;
    }
}
